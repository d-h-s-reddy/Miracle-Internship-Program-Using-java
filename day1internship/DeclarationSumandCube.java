package day1internship;

public class DeclarationSumandCube{
   public static void main(String[] args) {
	   //a)declare a variable
	   int j;
	   //b)Assign an value of the variable j
	   j=10;
	   //c)display the value stored inside the integer j
	   System.out.println("The value inside the variable j is:"+j);
	   //d)display the square of the content inside the variable j
	   System.out.println("The square of the given number is:"+(j*j));
	   //e)Display the cube of the content inside the variable j
	   System.out.println("The cube of the given number is:"+(j*j*j));
   }
}
